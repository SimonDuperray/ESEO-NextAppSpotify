class Album {
    constructor(artist_name, artist_uri, href, image, name, popularity, release_date, total_tracks, tracks) {
        this.artist_name = artist_name;
        this.artist_uri = artist_uri;
        this.href = href;
        this.image = image;
        this.name = name;
        this.popularity = popularity;
        this.release_date = release_date;
        this.total_tracks = total_tracks;
        this.tracks = tracks;
    }
}
